public class SwitchCase {
    public static void main(String[] args) {
        int number = 10;
        switch(number) {
            case 10:
               System.out.println("The number is 10");
               break;
            case 0:
                System.out.println("The number is 0");
                break;
            default:
                System.out.println("There is no number");
        }
    }
}
